  <h1>Rodent Control</h1><img width="240" height="160" src="images/rodents-crawl-space.jpg" align="right" hspace="12" /><br />
    There’s no such thing as just one mouse.  As <a href="http://www.rd.com/familyhandyman/"><em>The Family Handyman</em></a> has  reported, one female mouse is able to have as many as 10 litters in a single  year, with 6-7 new mice in each litter. Rodents can pass into even the  best-sealed homes, with mice entering spaces as small as a dime and rats  fitting into spaces the size of a quarter.</p>
  <p>Rodents carry a variety of bacteria and viruses, including  hantavirus and salmonella, and they can also spread parasites such as mites,  ticks, tapeworms and ringworms, according to Pennsylvania State  university experts.  <br />
    <br />
    Mice and rats can deal a lot of damage to a home, with <A href="http://www.realsimple.com"><em>Real Simple</em></A> warning  that they can gnaw through wires, chew through drywall, and become a  foul-smelling and unhealthy hazard once they’ve died behind your walls and  beneath your floorboards.  And wherever  mice and rats go, they leave a scent trail behind that will attract other mice  to the area. </p>
  <p><img width="276" height="179" src="images/mice-in-crawlspace.jpg" align="left" hspace="12" />Rats are larger, more  intelligent and much more sophisticated than mice, and they can be extremely  difficult to remove.  In an article on  post control within the food industry, <a href="http://www.poultryworld.org/"><em>Poultry World</em></a> has shown  that rats live in clans, with a dominant male leader, and they can even  recognize one another by the smell of their breath.  Rats are able to swim for two days, and they  can smell objects as much as 82 feet away.   Their teeth and jaws are strong enough to chew through almost anything  but steel.  In short, they’re stubborn,  resourceful, and determined survivors that are planning on living in your home  for a very long time.<br />
      <br />
      <img width="216" height="173" src="images/crawl-space-mouse-infestation.jpg" align="right" hspace="12" /><br />
    Mice and rats like dark, temperate spaces to build their  homes, and if your basement or crawl space is not protected, it’s like an open  invitation for them to set up camp.  Seal  the crawl space vents off and install a vapor barrier, and the door is no  longer open.</p>
  <p>Installing a car alarm doesn’t prevent a robber from  breaking in to your car, but it helps.   Likewise, installing a thick plastic liner in your crawl space or  basement won’t guarantee that mice or rats won’t enter the space.  However, it certainly lowers the likelihood that  these pests will find their way in, as they will first have to chew through the  airtight (and smell-tight) vapor barrier.   Along with helping to protect your home from humidity, rot, and high  utility bills, they are a great first line of defense against rodents, and a  great choice for your home.  <A href="contact.php">Encapsulate  your crawl space today</A>!<br clear="all" />
