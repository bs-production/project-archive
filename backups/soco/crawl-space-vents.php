  <h1>Crawl Space Vents</h1>
  <p><img width="199" height="150" src="images/crawl-space-vent.jpg" align="left" hspace="12" />In the winter, cold air  flows into the crawl space vents and upwards into your home.  Unfortunately, the crawl space is there for a  reason, and there are many things in the space that you really don’t need cold.  If you have a furnace, heating ducts, hot  water pipes or a water heater in the crawl space, you’ll be spending more on  utility bills as these objects fight to beat back the cold.  </p>
  <p>After everything in the crawl space is cooled, the cold will  them move upwards and seep through your flood boards (the cold air is rising  because of the stack effect).  If you  have insulation on your crawl space ceiling, this will help for a little while,  but the summer heat and humidity will ruin it within a few years (more on that  soon).  As the cold air moves through the  floorboards, it makes for a cold, uncomfortable flooring, and </p>
  <p><img width="200" height="164" src="images/moldly-rotting-crawl-space.jpg" align="left" hspace="12" />In the summer, hot,  humid summer air will pour into the crawl space continuously.  As this hot air enters your crawl space, it  will quickly cool underground.  As this  humid air cools, it will lose much of its ability to hold moisture, and this  extra water will be dropped on all surfaces in the area as condensation.</p>
  <p>And after the crawl space is soaked with moisture and  humidity, the air will (again) move upwards into your home.  And because air conditioners spend much of  their energy on dehumidification, it’s much harder to cool humid air than when  it’s dry.  As it adds humidity to the air  (as well as odors, mold spore allergens, dust mite waste allergens,  formaldehyde from fiberglass resins, and other pollutants), your air  conditioning bills will rise dramatically.</p>
  <p><img width="288" height="192" src="images/open-crawl-space-vent.jpg" align="left" hspace="12" />In the end, crawl space  vents have nothing to offer.  They add  heat and humidity in the summer and cold air in the winter, all of which adds  your home.  And even worse, as the humidity  fills the crawl space, you can expect a wide variety of new long-term problems  like mold, rot, dust mites, termites, rodents, carpenter ants, and the eventual  need to hire a contractor to crawl into this narrow space, rip out the  disgusting rotting wood, and replace it in cramped quarters.  All of these things combine to mean bad  things for your health and pocketbook!</p>
