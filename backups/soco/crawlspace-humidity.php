  <h1>Crawl Space Humidity</h1>
  <p>Crawl space humidity is a major problem in homes across the  world.  Humidity leads to an unhealthy  crawl space (which means an unhealthy home) and a rotting, damaged structure  supporting your home.  However, the  principal sources of <strong>crawl space humidity</strong>— namely crawl space vents, flooding  in crawl spaces, and water vapor passing in through the concrete and dirt crawl  space walls— are all possible to control with some simple solutions.  With a simple crawl space upgrade, your home  can become healthier, more comfortable, and much more energy-efficient.</p>
  <h2>The “Stack Effect”</h2>
  <p><img width="150" height="187" src="images/stack-effect.jpg" alt="Crawl Space Humidity Stack Effect" align="left" hspace="12" />When crawl spaces have  open vents, the air that flows into the space does not pass through the crawl  space in out the other vents.  Instead, a  large portion of the air will flow upwards through the floorboards (and  insulation) and enter your home.</p>
  <p>The reason is this:   as the air in your home heats, it naturally rises upwards, eventually  passing out of the house through the attic or upper levels.  As this happens, all the air in the house  will naturally shift upwards to fill in the space left by the departing  air.  This means that new air must move  into the home below.  If you have a  vented crawl space, some of this air will be pulled from that space.</p>
  <p>So the <strong>crawl space vents</strong> aren’t doing their job of creating  air circulation through the <strong>crawl space</strong>.   What are they doing instead?   Adding humidity to the crawl space in the summer and cooling the crawl  space in the winter.</p>
  <h2>Crawl Space Vents</h2>
  <p><img width="199" height="150" src="images/crawl-space-vent.jpg" alt="Crawl Space Vent Product" align="right" hspace="12" />In the winter, cold air  flows into the crawl space vents and upwards into your home.  Unfortunately, the crawl space is there for a  reason, and there are many things in the space that you really don’t need cold.  If you have a furnace, heating ducts, hot  water pipes or a water heater in the crawl space, you’ll be spending more on  utility bills as these objects fight to beat back the cold.  </p>
  <p>After everything in the crawl space is cooled, the cold will  them move upwards and seep through your flood boards (the cold air is rising  because of the stack effect).  If you  have insulation on your crawl space ceiling, this will help for a little while,  but the summer heat and humidity will ruin it within a few years (more on that  soon).  As the cold air moves through the  floorboards, it makes for a cold, uncomfortable flooring, and </p>
  <p><img width="200" height="164" src="images/moldly-rotting-crawl-space.jpg" alt="Crawl Space Mold and Rotting" align="left" hspace="12" />In the summer, hot,  humid summer air will pour into the crawl space continuously.  As this hot air enters your crawl space, it  will quickly cool underground.  As this  humid air cools, it will lose much of its ability to hold moisture, and this  extra water will be dropped on all surfaces in the area as condensation.</p>
  <p>And after the crawl space is soaked with moisture and  humidity, the air will (again) move upwards into your home.  And because air conditioners spend much of  their energy on dehumidification, it’s much harder to cool humid air than when  it’s dry.  As it adds humidity to the air  (as well as odors, mold spore allergens, dust mite waste allergens,  formaldehyde from fiberglass resins, and other pollutants), your air  conditioning bills will rise dramatically.</p>
  <p><img width="288" height="192" src="images/open-crawl-space-vent.jpg" align="right" hspace="12" />In the end, <strong>crawl space</strong>  vents have nothing to offer.  They add  heat and humidity in the summer and cold air in the winter, all of which adds  your home.  And even worse, as the humidity  fills the crawl space, you can expect a wide variety of new long-term problems  like mold, rot, dust mites, termites, rodents, carpenter ants, and the eventual  need to hire a contractor to crawl into this narrow space, rip out the  disgusting rotting wood, and replace it in cramped quarters.  All of these things combine to mean bad  things for your health and pocketbook!</p>
