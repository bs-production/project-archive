  <h1>Ant Infestations</h1>
    <p><img width="177" height="264" src="images/winged-ant-swarm-crawlspace.jpg" align="left" hspace="12" alt="Winged Ant Swarm in Crawl Space" />While they’re often  mistaken for termites, winged ants can easily be identified as different from  termites, as they will have two sets of wings, while termites have only one pair.  <A href="http://www.foodregulation.com/aius/home.jsp?pagetitle=aiusfp&amp;pubId=ag100"><em>Pesticide  &amp; Toxic Chemical News</em></a> has reported that the threat of  ant infestations is growing, and they now compete with termites as one of the  main household pests.  Ants can like in  and around your home almost anywhere, and with queen carpenter ants living as  long as 25 years, (and some species of ant growing in numbers to as much as  300,000 to 500,000 strong), ants are efficient, persistent, and plentiful once  established.  And because an ant can live  for ten days on a single crumb of food, even the cleanest homes can attract  ants into the space!</p>
  <div>
    <p><img width="252" height="189" src="images/ant-infestation-in-crawl-space.jpg" align="right" hspace="12" />Each species of ant has  developed its own sets of survival techniques that will help them survive in  your home.  As a prime example, <A href="  http://www.hotelmotel.com/"><em>Hotel and  Motel Management</em></a> warns that you should never use sprays  when dealing with Pharaoh ants.  These  sprays case the small brown ants to separate from their original colony, where  they sometimes morph into a new queen and start an entirely new colony!  Other species, such as fire ants, can rapidly  increase the size of their colony, with an increase in colony size of as much  as 500 new offspring in a day!</p>
    <p><img width="228" height="171" src="images/carpenter-ant-swarm-crawl-space.jpg" align="left" hspace="12" />Ants are famous for being  diligent, relentless, and effective at nearly everything they do.  Once they’ve entered your home, they’re going  to be a persistent and frustrating pest to kill.  However, you can take steps to help prevent  ants from living in your home.  One of  the best moves you can make to keep ants out of the home is to encapsulate your  crawl space.  By installing a crawl space  vapor barrier and sealing off all the crawl space vents, you take away one of  the places where ants can life, thrive, and survive.  A dehumidifier in the crawl space or basement  can dry out the wood and organic materials, which will make the home less  hospitable for carpenter ants and other pests.   <A href="http://www.socosupply.com/contact.php">Upgrade  your basement or crawl space today</a>!</p>
