  <h1>Termite Control</h1>
    <p><img width="432" height="249" src="images/termite-damaged-wood.jpg" align="right" class="img-right" />
Termites deal an enormous amount of damage to homes every  year- more than $30 billion annually in the United States alone!  A termite can fit through a space as small as  1/64th of an inch, and <a href=”http://www.popularmechanics.com">Popular Mechanics</a> has shown that termites are capable of moving  through any type of construction, including a concrete slab.  According to <a href="http://www.housingzone.com/probuilder">Professional  Builder Magazine</a> , termites most often enter the home through  pipe penetrations, bath traps, and construction joints, but they can also enter  underneath the siding or through crawl space vent openings.  To help keep them away from the home and to  keep the home visible for inspectors, try to keep the home’s slab visible for  about 6-8 inches on the bottom.</p>
  <p>They can eat a surprising variety of things, including  drywall paper, wood, leather, cardboard, books, cellulose insulation, and even  the furs of animals!  </p>
  <p><img src="images/termites1.jpg" align="left" class="img-left">Dealing with termites has become more and more challenging  as time has passed.  At one time, a  termite inspector could reasonably assume that the termites infesting a home were  one of a few local species and look for signs accordingly.  However, commerce brings wood to areas from  many parts of the world, and termites have begun to “hitchhike” along with it.  Combined with this, global warming is causing  termites to swarm less often, which is the most common way that homeowners  would detect them in the home.  Instead,  termites are reproducing by “budding” underground.  By budding, the colonies break off small  sections to become new colonies, all of which is done at depths of 10-20 feet  underground where homeowners cannot see them.   In some cases, these termites may not be detected by homeowners until  parts of the home are ready to collapse!</p>
  <p><img src="images/termites2.jpg" align="right" class="img-right">Other than swarming, it’s possible to look for termite  infestations by looking out for mud tubes, which are about the width of a  pencil and can run over foundation walls, support piers, sill plates, floor  joists, and other surfaces.  It’s also  wise to check for hollowed-out sections of wood.  If you have swarming termites, then it’s a  sign that the infestation may have been present for many years, and action is  very important.  Swarming termites cannot  eat and will not damage the wood.  If you  see them, collect a few to show inspectors, then vacuum the rest away.</p>
  <p>Termites are often attracted to your crawl space or basemei'm nnt  because it’s full of damp, rotting wood, which they love.  By sealing off your crawl space or basement  with a plastic barrier, pumping away any leaking water, and lowering the  humidity of the space with a good basement or crawl space dehumidifier, your  home can be much better protected from these pests.  <A href="contact.php">Upgrade your crawlspace or basement</a> today,  and save energy in the home will protecting yourself from pests.</p>
