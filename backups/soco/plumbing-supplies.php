  <h2>Sump Pump Kit</h2>
  <p><img width="224" height="312" src="images/plumbing-supplies-sump-pump.jpg" alt="Basement Sump Pump Plumbing" align="right" hspace="12" />The SoCo supply sump  pump kit includes a complete solution for your sump pump plumbing needs.  This sump pump kit is designed to be  installed in a sump pit in your basement or crawl space, in a bed of clean  gravel.</p>
  <p>The liner for this sump pump kit is designed to install in  your with no risk of rusting, rotting or corroding. Designed with durable  plastic with 96 weep holes drilled in the side, it helps to reduce clogs and is  airtight to keep humidity from rising out of the pit and into the basement.</p>
  <p>The lid of these sump pumps is extremely strong, with a  plastic airtight covering that is strong enough to stand on.  It attaches to the sump liner with four “easy  in” lag screws for an attractive design and easy access.  Rubber grommets attach around the cord  entrance and discharge pipe to complete the airtight seal and to help reduce  noise moving upwards from the sump into the basement or crawl space.</p>
