    <h1>Mold and Allergen Problems</h1>
    <p><img width="250" height="181" src="images/home-allergens-humidity.jpg" align="left" hspace="12" />Wet, 
      humid crawl spaces and basements mean problems with mold and rot underneath 
      your home. As the moisture sets in the space, all organic materials there 
      will become food to support mold and rot growth. While this mold, dust mites, 
      rot, and moisture is damaging your home and causing rot in your crawl space 
      insulation, wooden home structure, drywall, leather, basement carpet, and 
      all other organic things in the space, enormous allergens are also being 
      released into your basement. And with the <a href="http://www.socosupply.com/crawlspace-humidity.php">stack 
      effect</a> spreading these allergens throughout your home, we know that 
      you breathe the air in your crawl space and basement every day.</p>
    <br clear="left" />
    <p>Here are some of the common allergens you can expect to find in a crawl 
      space:<br />
    <ul>
      <br />
      <li>Mold spores</li>
      <li>Mildew</li>
      <li>Dust mite waste</li>
      <li>Allergens from cockroach sheddings and droppings</li>
      <li>Allergens from rodent dander and droppings</li>
    </ul></p>
    <p><img width="180" height="165" src="images/dust-mites.jpg" align="right" hspace="12" />Of 
      these five sources of allergens, mold, mildew, dust mites, and cockroaches 
      can all be discouraged by removing humidity from the air. Mold, mildew, 
      and dust mites all need to absorb humidity to survive, and American Cockroaches 
      need a continuous supply of readily-available moisture to survive. While 
      dehumidification will not completely eliminate cockroaches, it certainly 
      helps to keep the population down. Dehumidification as a means of cockroach 
      control (for the American Cockroach) has been recommended by the <a href="http://pested.unl.edu/pesticide/UserFiles/File/roach10.pdf">University 
      of Nebraska</a>, but only for cases in where the dehumidifier is self-draining 
      so that cockroaches cannot use the collection tray as a water source.</p>
    <p>In short, a single pair of cockroaches can create 500 young, a single dust 
      mite can lay up to 270 eggs in a lifetime, and mold releases spores by the 
      thousands as it reproduces. You want to create an environment in your home 
      where this cannot happen!</p>
    <p>Good Housekeeping Magazine has reported that 54 Americans suffer from allergies, 
      and the ICS Cleaning Specialist shows that there has been a 75% increase 
      in asthma and related health problems from 1980 to 2001 alone! Additionally, 
      the EPA ranks indoor air pollution as one of the top five environmental 
      risks to public health, and the American College of Allergy, Asthma, and 
      Immunology has found that 50% of all respiratory illnesses are aggravated 
      or caused by polluted indoor air.&nbsp;</p>
    <p><img width="252" height="179" src="images/moldy-drywall.jpg" align="left" hspace="12" />If 
      you suffer from allergy-related problems, here’s a test to see if indoor 
      allergens are causing health problems in your home. Watch carefully to see 
      when your allergy symptoms are at their worst. If you find that you’re suffering 
      from allergies most when you’re in your home- and the symptoms drop dramatically 
      when you leave the house- then it’s likely that there are allergens in your 
      home. The answer? <a href="dezair-crawlspace-dehumidifier.php">Install a 
      reliable basement dehumidifier</a>.</p>
    <p>By dropping the humidity below 60%, you can eliminate allergens in the 
      home from mold and dust mites and prevent the damage they cause. With the 
      <A href="dezair-crawlspace-dehumidifier.php">DezAir basement and crawl 
      space dehumidifier</A>, you can also filter the air itself, removing particles 
      such as pollen, dust mite feces, and even most mold spores. An optional 
      filter will also remove odors from the air, making your air dry, clean, 
      and fresh. <A href="contact.php">Call or contact us today for pricing and 
      product details on a basement or crawl space dehumidifier</A>!</p>
