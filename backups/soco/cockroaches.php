  <h1>Cockroaches</h1>
  <p><img width="252" height="189" src="images/cockroach-crawl-space.jpg" align="left" hspace="12" />Other than being  disgusting, disturbing creatures to have scuttling around the home, cockroaches  are also carriers of pathogens such as harmful bacteria (Salmonella) and  unhealthy fungi.  As they walk on food or  countertops, they can transfer these organisms to be ingested by people living  in the home.  </p>
  <p>To make matters worse, the salvia, feces, outer covering,  and eggs of cockroaches have been reported by Agricultural Research Magazine to  be highly allergenic to those with asthma and respiratory conditions.  These allergens can linger even after the  infestation has been eliminated, with studies showing that the allergens are  still present even years later.  And with  a single cockroach being able to produce as many as 500 young in a year,  there’ll be a lot of these allergens to go around!<br />
